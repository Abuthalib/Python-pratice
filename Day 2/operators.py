# arithmetic operators #

a = 5
b = 3

print(a+b)
print(a-b)
print(a*b)
print(a/b)
print(a//b)
print(a%b)

# Relational operators or comparsion operators #
# it always return bolean value
print(a > b)
print(a < b)
print(a == b)
print(a != b)
print(a <= b)
print(a >= b)

# logical operators#
# and,or,not
# it always return bolean value
a = True
b = False

